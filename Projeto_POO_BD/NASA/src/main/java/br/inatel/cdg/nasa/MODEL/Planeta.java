package br.inatel.cdg.nasa.MODEL;

public class Planeta
{
    private int idPlaneta;
    private String nome;
    private boolean habitavel;

    // Getters e Setters
    public int getIdPlaneta() {return idPlaneta;}
    public void setIdPlaneta(int idPlaneta) {this.idPlaneta = idPlaneta;}
    public String getNome() {return nome;}
    public void setNome(String nome) {this.nome = nome;}
    public boolean isHabitavel() {return habitavel;}
    public void setHabitavel(boolean habitavel) {this.habitavel = habitavel;}

}
